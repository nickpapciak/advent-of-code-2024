    # counts rows going forward, 
    # the two downward diagonals
    # and cols going down
    def count_xmas(data): 
        
        # returns none if not in bounds
        def in_bounds(i, j): 
            if i >= 0 and j >= 0 and \
               i < len(data) and j < len(data[0]): 
                return data[i][j]

        # northwest diagonal
        def check_diag_sw(i, j): 
            return in_bounds(i+1, j-1) == "M" \
               and in_bounds(i+2, j-2) == "A" \
               and in_bounds(i+3, j-3) == "S"
            
        # southeast diagonal
        def check_diag_se(i, j): 
            return in_bounds(i+1, j+1) == "M" \
               and in_bounds(i+2, j+2) == "A" \
               and in_bounds(i+3, j+3) == "S"
            
        def check_row(i, j): 
            return in_bounds(i, j+1) == "M" \
               and in_bounds(i, j+2) == "A" \
               and in_bounds(i, j+3) == "S"
            
        def check_col(i, j): 
            return in_bounds(i+1, j) == "M" \
               and in_bounds(i+2, j) == "A" \
               and in_bounds(i+3, j) == "S"
     
        count = 0
        data = data.splitlines()
        
        for i in range(len(data)): 
            for j in range(len(data[0])): 
                if data[i][j] == 'X': 
                    count += check_diag_sw(i, j)
                    count += check_diag_se(i, j)
                    count += check_row(i, j)
                    count += check_col(i, j)
        return count

    # 2573
    print(count_xmas(data) + count_xmas(data[::-1]))
    return (count_xmas,)


